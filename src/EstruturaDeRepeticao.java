import java.util.Scanner;

public class EstruturaDeRepeticao {

	public static void main(String[] args) {
		
//		utilizando o while
		int contador = 0;
		while (contador <= 10) {
			System.out.println("Repetição While " + contador + " de 10");
			contador++;
		}
		
		System.out.println("\n-------------------------------");
	}
}
